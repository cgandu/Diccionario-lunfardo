def ConsigoLineas(t):
    arrayDeLineas = re.split("\n", t)
    return arrayDeLineas

def ConstruyoDiccionario(texto):
    global entradas
    global diccionario
    idxEntradas = 0
    lineas = ConsigoLineas(texto)
    
    for x in lineas:
       
        if ":" in x:
            
            entradas.append(x)
            idxEntradas = idxEntradas + 1
        else:
            entradas[len(entradas)-1] = entradas[len(entradas)-1] + " " + x

    for entrada in entradas:
        temp = re.split(":", entrada)
        
        diccionario[temp[0].lower()] = [temp[1]]

    
    return diccionario


